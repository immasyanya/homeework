month = int(input('Введите число от 1 до 12: '))

my_dict = {0: 'зима', 1: 'весна', 2: 'лето', 3: 'осень', 4: 'зима'}

my_list = list(['январь', 'февраль', 'март', 'апрель', 'май', 'июнь', 'июль', 'август', 'сентябрь', 'октябрь', 'ноябрь',
                'декабрь'])

if 1 < month < 12:
    print(f'{month}-й месяц года - это {my_dict.get(month // 3)}, месяц {my_list[int(month) - 1]}')
else:
    print('Вы ввели неправильное число')